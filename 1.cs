/*C#程序1：变量*/
using System;

namespace Program_1{
    class Program{
        static void Main(string[] args){
            int a=10;
            double b=2.5;
            bool c=true;
            char d='a';
            string e="abcd";

            Console.WriteLine("输出整数{0}",a);
            Console.WriteLine("输出浮点数（双精度）{0}",b);
            Console.WriteLine("输出布尔值{0}",c);
            Console.WriteLine("输出字符{0}",d);
            Console.WriteLine("输出字符串{0}",e);
        }
    }
}
